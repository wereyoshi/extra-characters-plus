    end

    if a.action == ACT_WARIO_AIR_DASH then
        wario_rebound(a, -40, 15)
    end
end

function convert_s16(num)
    local min = -32768
    local max = 32767
    while (num < min) do
        num = max + (num - min)
    end
    while (num > max) do
        num = min + (num - max)
    end
    return num
end

